/**
 * @file Определяет структуру данных для пользователя.
 */

/**
 * Интерфейс, описывающий объект пользователя.
 */
export interface User {
  /**
   * Уникальный идентификатор пользователя.
   * Может быть числом или строкой, в зависимости от источника (например, из Google Таблицы).
   */
  id: string | number;
  /** Логин пользователя. */
  login: string;
  /** Хеш пароля пользователя (или сам пароль в простых случаях). Опционально. */
  passwordHash?: string;
  /** Имя пользователя (опционально). */
  firstName?: string;
  /** Отчество пользователя (опционально). */
  middleName?: string;
  /** Фамилия пользователя (опционально). */
  lastName?: string;
  /** Должность пользователя (опционально). */
  position?: string;
  /**
   * Цвет иконки пользователя в HEX-формате (например, "#32a852") (опционально).
   * Используется для кастомизации аватара пользователя.
   */
  iconColor?: string;
}
