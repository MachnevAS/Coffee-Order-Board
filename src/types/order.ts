/**
 * @file Определяет типы данных, связанные с заказами и историей продаж.
 */

/**
 * Интерфейс для элемента в истории продаж (или в составе заказа).
 * Представляет отдельный товар в заказе с указанием его количества и цены на момент продажи.
 */
export interface SalesHistoryItem {
  /** Уникальный идентификатор товара в рамках заказа (может быть ID продукта или сгенерированный). */
  id: string;
  /** Название товара. */
  name: string;
  /** Цена товара за единицу на момент продажи. */
  price: number;
  /** Количество данного товара в заказе. */
  quantity: number;
  /** Объем товара (например, "0,2 л", "50 мл"), если применимо. */
  volume?: string;
}

/**
 * Тип, представляющий возможные способы оплаты заказа.
 */
export type PaymentMethod = 'Наличные' | 'Карта' | 'Перевод';

/**
 * Интерфейс для объекта заказа.
 * Содержит всю информацию о конкретном заказе.
 */
export interface Order {
  /** Уникальный идентификатор заказа. */
  id: string;
  /** Массив товаров, входящих в заказ. */
  items: SalesHistoryItem[];
  /** Общая стоимость заказа. */
  totalPrice: number;
  /** Временная метка создания заказа (в формате строки, например, "dd.MM.yyyy HH:mm:ss"). */
  timestamp: string;
  /** Способ оплаты заказа. */
  paymentMethod: PaymentMethod;
  /** Информация о сотруднике, оформившем заказ (например, "Должность - Фамилия И.О. (логин)"). */
  employee?: string;
}
